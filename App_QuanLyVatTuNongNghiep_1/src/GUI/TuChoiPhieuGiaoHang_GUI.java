    private String layLyDo()
    {
        String lydo = "";
        for (Component com : panel_lydo.getComponents()) {
            JCheckBox chk = (JCheckBox)com;
            if(chk.isSelected())
                lydo+=chk.getText()+". ";
        }
        if(chk_khac.isSelected())
            lydo+=textPane.getText().trim()+".";
        return lydo;
    }

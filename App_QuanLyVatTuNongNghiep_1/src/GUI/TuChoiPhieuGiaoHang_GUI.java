    private void btn_xacNhanHuyActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_xacNhanHuyActionPerformed
        // TODO add your handling code here:
        JFrame frame = (JFrame) SwingUtilities.getWindowAncestor(this);
        
        String lydo = layLyDo();
        if(lydo.equals(""))
        {
            //JOptionPane.showMessageDialog(this, "Phải có ít nhất 1 lý do hủy phiếu giao hàng!!!");
            Notification panel = new Notification(frame, Notification.Type.WARNING, Notification.Location.TOP_RIGHT, "Phải có ít nhất 1 lý do hủy phiếu giao hàng!!!");
            panel.showNotification();
            return;
        }
        pgh.setTrangThai("Bị hủy");
        pgh.setGhiChu(lydo);
        System.out.println(lydo);
        if(db.getPhieuGiaoHangs().update(pgh))
            //JOptionPane.showMessageDialog(this, "Thao tác thành công");
        {
            Notification panel = new Notification(frame, Notification.Type.SUCCESS, Notification.Location.TOP_RIGHT,  "Thao tác thành công");
            panel.showNotification();
        }
        else
        {
            Notification panel = new Notification(frame, Notification.Type.WARNING, Notification.Location.TOP_RIGHT,  "Hủy phiếu giao hàng thất bại");
            panel.showNotification();
        }
            //JOptionPane.showMessageDialog(this, "Hủy phiếu giao hàng thất bại");
        this.qlgh.locTheoTrangThai();
        this.qlgh.setsetEnabledFalse();
        this.setVisible(false);
    }//GEN-LAST:event_btn_xacNhanHuyActionPerformed


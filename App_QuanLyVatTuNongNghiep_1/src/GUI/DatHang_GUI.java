    private void btn_capNhatActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_capNhatActionPerformed
        // TODO add your handling code here:
        String strSL = txt_soLuong.getText();
        if(strSL.isEmpty())
        {
            JOptionPane.showMessageDialog(this,"Số lượng mua không được để trống!!!");            
            return;
        }        
        int sl = Integer.parseInt(strSL);  
        NguonHang nh = getNguonHangSelect();
        int i = 0;
        int gia = nh.getGiaNhap();
        for(Object item : dtm.getDataVector())
        {
            Vector v = (Vector)item;
            if(v.get(0).toString() == nh.getMaCTLH())
            {              
                sl = idxSua == -1 ? (int) v.get(4)+sl:sl;                
                v.set(4,sl);                
                v.set(3, gia); 
                v.set(5,gia*sl);
                //trường hợp sửa
                if(idxSua != -1 && idxSua != i)
                {
                    dtm.removeRow(idxSua);                    
                }
                tbl_datHang.updateUI();
                btn_huyActionPerformed(null);
                return;
            }
            i ++;
        }                  
            Vector ctdh = new Vector();
            ctdh.add(nh.getMaCTLH());
            ctdh.add(nh.getChiTietLoaiHang().getHangHoa().getTenHang());
            ctdh.add(nh.getChiTietLoaiHang().getTenLoai());
            ctdh.add(gia);
            ctdh.add(sl);
            ctdh.add(gia*sl); 
            if(idxSua==-1)
                dtm.addRow(ctdh);                
            else
            {                
                dtm.removeRow(idxSua);
                dtm.insertRow(idxSua, ctdh);
                tbl_datHang.updateUI();
                btn_huyActionPerformed(null);
            }
    }//GEN-LAST:event_btn_capNhatActionPerformed


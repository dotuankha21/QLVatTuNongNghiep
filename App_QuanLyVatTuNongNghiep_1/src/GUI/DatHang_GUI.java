    private void btn_datHangActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_datHangActionPerformed
        // TODO add your handling code here:
        JFrame frame = (JFrame) SwingUtilities.getWindowAncestor(this);
        if(dtm.getRowCount()==0)
        {
            Notification panel = new Notification(frame, Notification.Type.INFO, Notification.Location.TOP_RIGHT, "Cần phải có ít nhất 1 chi tiết đơn đặt hàng!!!");
            panel.showNotification();
            //JOptionPane.showMessageDialog(this, "Cần phải có ít nhất 1 chi tiết đơn đặt hàng!!!"); 
            return ;
            
        }
        if(JOptionPane.showConfirmDialog(this, "Xác nhận đặt hàng?","Thông báo",JOptionPane.YES_NO_OPTION)==JOptionPane.YES_OPTION)
        {
            String soDonDatHang = null;
            int sl = 1;
            do
            {
                int slHD = db.getPhieuDatHangs().size()+1;
                String soddh = soDonDatHang = "DDH00"+String.valueOf(slHD+sl++);    
                if(db.getPhieuDatHangs().get(t->t.getSoDonDatHang().contains(soddh)) == null)
                {
                    soDonDatHang = soddh;
                    break;
                }
            } while(true);  
            int tongSl = 0;
            PhieuDatHang pdhMoi = new PhieuDatHang(soDonDatHang,maNCCSelect, user.getMaNV());
            int tongTien = 0;
            ArrayList<ChiTietDatHang> ctdhs = new ArrayList<ChiTietDatHang>();
            for (Object item : dtm.getDataVector()) {
                Vector v = (Vector) item;
                ChiTietDatHang ctdh = new ChiTietDatHang(soDonDatHang, (String) v.get(0), (int) v.get(3), (int) v.get(4));
                tongSl+=(int) v.get(4);
                tongTien += (int)v.get(5);
                ctdhs.add(ctdh);
            }
            pdhMoi.setTongSL(tongSl);        
            pdhMoi.setThanhTien(tongTien);            
            if(db.getPhieuDatHangs().insert(pdhMoi))
            {
                int thanhcong = db.getChiTietDatHangs().insert(ctdhs);
                if(thanhcong == ctdhs.size())     
                {
                    Notification panel = new Notification(frame, Notification.Type.SUCCESS, Notification.Location.TOP_RIGHT, "Bạn đã đặt hàng thành công");
                    panel.showNotification();
                    //JOptionPane.showMessageDialog(this, "Bạn đã đặt hàng thành công");
                }                                                        
                else                
                {
                    Notification panel = new Notification(frame, Notification.Type.INFO, Notification.Location.TOP_RIGHT, thanhcong + " chi tiết đặt hàng đầu tiên được tạo!!!");
                    panel.showNotification();
                    //JOptionPane.showMessageDialog(this, thanhcong + " chi tiết đặt hàng đầu tiên được tạo!!!");
                }                                    
                dtm.setRowCount(0);
                return;
            }
                Notification panel = new Notification(frame, Notification.Type.WARNING, Notification.Location.TOP_RIGHT, "Thao tác thất bại");
                    panel.showNotification();
            //JOptionPane.showMessageDialog(this, "Thao tác thất bại");            
        }
        
    }//GEN-LAST:event_btn_datHangActionPerformed

    

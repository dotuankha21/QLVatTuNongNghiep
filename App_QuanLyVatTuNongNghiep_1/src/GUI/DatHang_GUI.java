    private void cbo_nccActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cbo_nccActionPerformed
        // TODO add your handling code here:
        if(cbo_ncc.getSelectedIndex()<0)
            return;
        String maNCCSelect_tam = getMaNCCSelect();
        if(maNCCSelect_tam.equals(maNCCSelect))
            return;
        try
        {
            if(dtm.getRowCount() == 0 ||(dtm.getRowCount() > 0 && JOptionPane.showConfirmDialog(this, "Nếu chọn nhà cung cấp mới, việc lập các dòng đặt hàng trước đó sẽ bị xóa!!!\nXác nhận tiếp tục?","Thông báo",JOptionPane.YES_NO_OPTION)==JOptionPane.YES_OPTION))
            {
                dtm.setRowCount(0);
                maNCCSelect = maNCCSelect_tam;     
                loadDuLieuNguonHang();
            }
        }catch(Exception e){
            maNCCSelect = maNCCSelect_tam;     
            loadDuLieuNguonHang();
        }
    }//GEN-LAST:event_cbo_nccActionPerformed

